const getBlogData = async (id: number) => {
  const res = await fetch(`https://api.slingacademy.com/v1/sample-data/blog-posts/${id}`);
  if (!res.ok) {
    throw new Error("Could not retrieve blog posts");
  }
  return await res.json();
};

